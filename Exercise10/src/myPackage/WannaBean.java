package myPackage;

public class WannaBean {
	int cnt = 0;
	String firstHost = "";
	String firstUserAgent = "";
	String Host = "";
	String UserAgent = "";
	
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public String getFirstHost() {
		return firstHost;
	}
	public void setFirstHost(String firstHost) {
		this.firstHost = firstHost;
	}
	public String getFirstUserAgent() {
		return firstUserAgent;
	}
	public void setFirstUserAgent(String firstUserAgent) {
		this.firstUserAgent = firstUserAgent;
	}
	public String getHost() {
		return Host;
	}
	public void setHost(String secondHost) {
		this.Host = secondHost;
	}
	public String getUserAgent() {
		return UserAgent;
	}
	public void setUserAgent(String secondUserAgent) {
		this.UserAgent = secondUserAgent;
	}
	
	
}
